from __future__ import annotations

from copy import deepcopy


def _step(
    index: int,
    tool: str,
    args: dict,
    observation: dict,
    note: str,
    *,
    severity: str = "success",
    reward_delta: float = 0.0,
    hard_fail: bool = False,
) -> dict:
    return {
        "index": index,
        "tool": tool,
        "args": args,
        "rationale": note,
        "ok": not hard_fail,
        "blocked": hard_fail,
        "observation": observation,
        "state_delta": {},
        "verifier_notes": [
            {
                "severity": severity,
                "code": {
                    "success": "STEP_OK",
                    "warning": "STEP_WARNING",
                    "error": "STEP_ERROR",
                }.get(severity, "STEP_OK"),
                "message": note,
            }
        ],
        "reward_delta": reward_delta,
        "hard_fail": hard_fail,
        "latency_ms": 120 + (index * 15),
    }


def _reward_item(component: str, label: str, points: float, reason: str, earned: bool = True) -> dict:
    return {
        "component": component,
        "label": label,
        "points": points,
        "earned": earned,
        "reason": reason,
    }


def _episode(
    task_id: str,
    agent_id: str,
    *,
    mode: str,
    steps: list[dict],
    success: bool,
    reward: float,
    hard_fail: bool,
    fail_reasons: list[str],
    success_reasons: list[str],
    final_state: dict,
    final_report: dict,
    reward_breakdown: list[dict],
    started_at: str,
    ended_at: str,
    episode_suffix: str,
) -> dict:
    return {
        "schema_version": "forge.trace.v1",
        "episode_id": f"demo_{task_id}_{agent_id}_{episode_suffix}",
        "task_id": task_id,
        "agent_id": agent_id,
        "mode": mode,
        "seed": 42,
        "started_at": started_at,
        "ended_at": ended_at,
        "steps": steps,
        "final_state": final_state,
        "final_report": final_report,
        "verifier_result": {
            "success": success,
            "reward": reward,
            "hard_fail": hard_fail,
            "reward_breakdown": reward_breakdown,
            "fail_reasons": fail_reasons,
            "success_reasons": success_reasons,
            "metrics": {
                "manual_lookup": any(step["tool"] == "read_manual" for step in steps),
                "inventory_check": any(step["tool"] == "check_inventory" for step in steps),
                "safety_violation": hard_fail,
                "report_complete": bool(final_report),
                "steps": len(steps),
            },
        },
    }


def _comparison(baseline: dict, improved: dict) -> dict:
    return {
        "baseline": baseline,
        "improved": improved,
    }


